#ifndef   STD_TYPES_H
#define   STD_TYPES_H

typedef unsigned 	   char       u8;       //1byte
typedef unsigned short int        u16;      //2byte
typedef unsigned long  int        u32;      //4byte

typedef signed         char       s8;       //1byte
typedef signed short   int   	  S16;      //2byte
typedef signed long    int        s32;      //4byte

typedef                float      f32;      //4byte   by default Signed
typedef                double 	  f64;      //8byte	  by default Signed

#endif
